list = [2 , 1 ,3]
list.append(4)          #this will add a new element at the end of the list

list.sort()             #this will sort the list in ascending order

list.reverse()          #this will reverse the list

list.insert(1, 5)       #this will insert the element 5 at index 1
list.insert(3,7)        #this will insert the element 7 at index 3
list.insert(7,10)       #this will insert the element 10 at index 7
print(list[7])          #this will print the element at index 7 which is 10
print(list[8])          #this will produce an error because there is no index 8 in the list.